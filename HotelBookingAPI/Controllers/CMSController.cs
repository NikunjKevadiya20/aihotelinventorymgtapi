        #region CMS Image Update

        [HttpPost("CMSImageUpdate")]
        public async Task<IActionResult> CMSImageUpdate([FromForm] CMSImageDataViewEntity docs)
        {
            try
            {
                int updatedBy = 1;
                string physicalFileName = string.Empty;

                if (docs.CMSImage != null && docs.CMSImage.Length > 0)
                {
                    string fileName = Path.GetFileName(docs.CMSImage.FileName);
                    string fileExtension = Path.GetExtension(fileName);
                    physicalFileName = $"{DateTime.Now.Ticks}.webp";

                    string folderPath = Path.Combine(
                        CommonRepositoryConstants.ImageFilePath,
                        CommonRepositoryConstants.documentsFolder
                    );

                    if (!Directory.Exists(folderPath))
                        Directory.CreateDirectory(folderPath);

                    string filePath = Path.Combine(folderPath, physicalFileName);

                    await ImageHelper.CompressWithSkia(docs.CMSImage, filePath);
                }

                var result = await domain.CMSImageUpdate(physicalFileName, docs.CMSImageAlterTag, docs.ID, updatedBy);

                return Ok(new ResultModel()
                {
                    Status = (int)ResponseStatusCode.Success,
                    Message = result.Message,
                    Details = result.Details,
                    Data = result,
                    ErrorMessage = result.ErrorMessage
                });
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Error in CMSImageUpdate");

                return StatusCode((int)HttpStatusCode.InternalServerError, new ResultModel()
                {
                    Status = (int)ResponseStatusCode.InternaServerError,
                    Message = CommonRepositoryMessages.NotFoundMessageEN,
                    Details = CommonRepositoryMessages.NotFoundMessageEN,
                    ErrorMessage = ex.Message
                });
            }
        }

