        public async Task<BlogListEntity> BlogFindByURL(BlogUrlEntity entity, string storedProcedure)
        {
            BlogListEntity result = new BlogListEntity();
            try
            {
                Dapper.DefaultTypeMap.MatchNamesWithUnderscores = true;
                DynamicParameters dynamicParameters = new DynamicParameters();
                dynamicParameters.Add("@BlogURL", entity.BlogURL);
                dynamicParameters.Add("@OperationType", 2);
               
                using var data = await _dbConnection.QueryMultipleAsync(
                   storedProcedure,
                   dynamicParameters,
                   commandType: CommandType.StoredProcedure);

                var blog = await data.ReadFirstOrDefaultAsync<BlogListEntity>();

                if (blog != null)
                {
                    blog.BlogFAQList = (await data.ReadAsync<BlogFAQList>()).ToList();
                }

                return blog;

            }
            catch (SqlException sqlException)
            {
