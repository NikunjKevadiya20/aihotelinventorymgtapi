        #region Property List With Video

        public async Task<List<PropertyEntitys>> PropertyListWithVideo(string storedProcedure)
        {
            PropertyEntitys result = new PropertyEntitys();
            try
            {
                Dapper.DefaultTypeMap.MatchNamesWithUnderscores = true;
                DynamicParameters dynamicParameters = new DynamicParameters();
                dynamicParameters.Add("@operationtype", 7);
                var data = await _dbConnection.QueryMultipleAsync(storedProcedure, dynamicParameters, commandType: CommandType.StoredProcedure);

                var properties = (await data.ReadAsync<PropertyEntitys>()).ToList();

                // Second result set → Gallery images
                var galleryvideo = (await data.ReadAsync<GalleryVideoViewEntity>()).ToList();

                // Map gallery images to property
                foreach (var property in properties)
                {
                    property.GalleryVideoList = galleryvideo
                        .Where(x => x.ReferenceID == property.ID)
                        .ToList();
                }

                return properties;
            }
            catch (SqlException sqlException)
            {
                logger.LogError(sqlException, sqlException.Message);
                result.ErrorMessage = sqlException.Message;
                result.Status = (int)ResponseStatusCode.InternaServerError;
                result.Message = CommonRepositoryMessages.CannotFindAllMessage;
